public class Loops {
    public static void main(String[] args) {
        int[] arraySimples = {1, 2, 3, 4};

        for (int i = 0; i < arraySimples.length; i++) { //Estrutura de Repetição (For Loop)
            System.out.println(arraySimples[i]);

        }

        //System.out.println(arraySimples.length);
    }
}
